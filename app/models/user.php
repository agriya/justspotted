    public function checkUsernameAvailable($username)
    {
		$username = str_replace(' ', '', $username);
		$username = str_replace('.', '_', $username);
        $user = $this->find('count', array(
            'conditions' => array(
                'User.username' => $username
            ) ,
            'recursive' => - 1
        ));
        if (!empty($user)) {
            return false;
        }
        return $username;
    }
